class Instrument {
  int id;
  String name;
  String category;
  int quantity;
  int available;
  String status;
  String condition;
  String location;

  Instrument({
    required this.id,
    required this.name,
    required this.category,
    required this.quantity,
    required this.available,
    required this.status,
    required this.condition,
    required this.location,
  });
}
